public class ControleFluxo1
{
  public static void main(String[] args)
  {
    int number = 2;

    if(number <= 3)
    {
      System.out.println("Certo");
    }
    else if(number == 4)
    {
      System.out.println("Errador");
    }
    else if(number == 5)
    {
      System.out.println("Talvez");
    }
    else
    {
      System.out.println("Valor indefinido");
    }
    

  }
}
